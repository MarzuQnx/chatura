    var supportedLangs = ['en', 'id'];
    var currentLang = supportedLangs.indexOf(localStorage.getItem('lang')) !== -1 ? localStorage.getItem('lang') : 'en';
    var translations = {};

    function setText(el, text) {
        var textNodes = [];
        for (var i = 0; i < el.childNodes.length; i++) {
            if (el.childNodes[i].nodeType === 3) textNodes.push(el.childNodes[i]);
        }
        if (textNodes.length === 0) {
            el.insertBefore(document.createTextNode(text), el.firstChild);
        } else if (textNodes.length === 1) {
            textNodes[0].textContent = text;
        } else {
            textNodes[0].textContent = text;
            for (var j = 1; j < textNodes.length; j++) textNodes[j].textContent = '';
        }
    }

    function applyTranslations(data) {
        translations = data;
        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var key = el.getAttribute('data-i18n');
            if (data[key] !== undefined) setText(el, data[key]);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-placeholder');
            if (data[key] !== undefined) el.placeholder = data[key];
        });
        document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-title');
            if (data[key] !== undefined) el.title = data[key];
        });
        document.documentElement.lang = currentLang;
        document.title = data['site.title'] || document.title;
        window.dispatchEvent(new CustomEvent('langChange', { detail: { lang: currentLang, data: data } }));
    }

    function updateLangSelector(lang) {
        var desktopSelector = document.getElementById('lang-selector');
        if (desktopSelector) updateSelectorLinks(desktopSelector, lang);

        var mobileSelector = document.getElementById('lang-selector-mobile');
        if (mobileSelector) updateSelectorLinks(mobileSelector, lang);

        var portalSelector = document.querySelector('.portal-footer-lang');
        if (portalSelector) updateSelectorLinks(portalSelector, lang);
    }

    function updateSelectorLinks(selector, lang) {
        selector.querySelectorAll('a').forEach(function (a) {
            var linkLang = (a.getAttribute('data-lang') || a.textContent).trim().toLowerCase();
            if (linkLang === lang) {
                a.className = 'lang-active';
            } else if (linkLang === 'en' || linkLang === 'id') {
                a.className = 'lang-inactive';
            }
        });
    }

    function setLang(lang) {
        if (supportedLangs.indexOf(lang) === -1) return;
        currentLang = lang;
        localStorage.setItem('lang', lang);
        updateLangSelector(lang);
        applyTranslations(I18N_DATA[lang]);
    }

    function bindSelector(selector) {
        selector.querySelectorAll('a').forEach(function (a) {
            a.addEventListener('click', function (e) {
                e.preventDefault();
                var newLang = (a.getAttribute('data-lang') || a.textContent).trim().toLowerCase();
                setLang(newLang);
            });
        });
    }

    function init() {
        ['lang-selector', 'lang-selector-mobile'].forEach(function (id) {
            var selector = document.getElementById(id);
            if (selector) bindSelector(selector);
        });

        var portalSelector = document.querySelector('.portal-footer-lang');
        if (portalSelector) bindSelector(portalSelector);

        setLang(currentLang);
    }

    window.setLang = setLang;
    window.getTranslations = function () { return translations; };

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
